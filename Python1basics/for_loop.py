#for loop= used to iterate though a sequence:list,tulples or a string
#looping through a range
for x in range(6):
    print(x)
#loop through range=10-15
for y in range(10,16):
    print(y)
# loop through a string
for z in"hello":
    print(z)
            
#loop through a list
      
fruits =["apple", "banana" ,"mangoes", "oranges"]
for a in fruits:
    print(a)
                
