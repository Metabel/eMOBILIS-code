fun main() {
    val myrange=1 until 5
    for (x in myrange){
        println(x)
    }
    for(num in 10 until 19){
        print("$num")
    }
    for (char in 'c' until 'n'){
        println(char)
    }
    for (i in 2 until 10 step 2){
        println(i)
    }
    for(z in 15 until 7){
        println(z)
    }


}